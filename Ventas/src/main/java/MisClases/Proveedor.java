
package MisClases;


public class Proveedor {
    private int IdProveedor;
    private String PvNombre;
    private String PvDireccion;
    private int PvNumero;
    private String PvCorreo;

    public int getIdProveedor() {
        return IdProveedor;
    }

    public void setIdProveedor(int IdProveedor) {
        this.IdProveedor = IdProveedor;
    }

    public String getPvNombre() {
        return PvNombre;
    }

    public void setPvNombre(String PvNombre) {
        this.PvNombre = PvNombre;
    }

    public String getPvDireccion() {
        return PvDireccion;
    }

    public void setPvDireccion(String PvDireccion) {
        this.PvDireccion = PvDireccion;
    }

    public int getPvNumero() {
        return PvNumero;
    }

    public void setPvNumero(int PvNumero) {
        this.PvNumero = PvNumero;
    }

    public String getPvCorreo() {
        return PvCorreo;
    }

    public void setPvCorreo(String PvCorreo) {
        this.PvCorreo = PvCorreo;
    }
    
    
}
