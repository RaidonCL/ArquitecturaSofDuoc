
package MisClases;


public class Empresa {
    private int EmpRut;
    private String EmpNombre;
    private String DircEmpresa;

    public int getEmpRut() {
        return EmpRut;
    }

    public void setEmpRut(int EmpRut) {
        this.EmpRut = EmpRut;
    }

    public String getEmpNombre() {
        return EmpNombre;
    }

    public void setEmpNombre(String EmpNombre) {
        this.EmpNombre = EmpNombre;
    }

    public String getDircEmpresa() {
        return DircEmpresa;
    }

    public void setDircEmpresa(String DircEmpresa) {
        this.DircEmpresa = DircEmpresa;
    }
    
}
