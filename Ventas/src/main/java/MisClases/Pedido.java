
package MisClases;


public class Pedido {
    private int IdPedido;
    private int PFecha;
    private String Estado;

    public int getIdPedido() {
        return IdPedido;
    }

    public int getPFecha() {
        return PFecha;
    }

    public String getEstado() {
        return Estado;
    }

    public void setIdPedido(int IdPedido) {
        this.IdPedido = IdPedido;
    }

    public void setPFecha(int PFecha) {
        this.PFecha = PFecha;
    }
    
    
}
