
package MisClases;


public class Factura {
    private int IdFactura;
    private int Ffecha;
    private int Ftotal;

    public int getIdFactura() {
        return IdFactura;
    }

    public int getFfecha() {
        return Ffecha;
    }

    public int getFtotal() {
        return Ftotal;
    }

    public void setFfecha(int Ffecha) {
        this.Ffecha = Ffecha;
    }

    public void setFtotal(int Ftotal) {
        this.Ftotal = Ftotal;
    }
    
    
}
