
package MisClases;


public class Envio {
    private int IdEnvio;
    private String DirecEntrega;
    private int EnvFecha;
    private String EstEnvio;

    public int getIdEnvio() {
        return IdEnvio;
    }

    public void setIdEnvio(int IdEnvio) {
        this.IdEnvio = IdEnvio;
    }

    public String getDirecEntrega() {
        return DirecEntrega;
    }

    public void setDirecEntrega(String DirecEntrega) {
        this.DirecEntrega = DirecEntrega;
    }

    public int getEnvFecha() {
        return EnvFecha;
    }

    public void setEnvFecha(int EnvFecha) {
        this.EnvFecha = EnvFecha;
    }

    public String getEstEnvio() {
        return EstEnvio;
    }

    public void setEstEnvio(String EstEnvio) {
        this.EstEnvio = EstEnvio;
    }
    
    
}
