
package MisClases;


public class Trabajador {
    private int IdTrabajador;
    private int TrabRut;
    private String TrabNombre;
    private String TrabTelefono;

    public int getIdTrabajador() {
        return IdTrabajador;
    }

    public void setIdTrabajador(int IdTrabajador) {
        this.IdTrabajador = IdTrabajador;
    }

    public int getTrabRut() {
        return TrabRut;
    }

    public void setTrabRut(int TrabRut) {
        this.TrabRut = TrabRut;
    }

    public String getTrabNombre() {
        return TrabNombre;
    }

    public void setTrabNombre(String TrabNombre) {
        this.TrabNombre = TrabNombre;
    }

    public String getTrabTelefono() {
        return TrabTelefono;
    }

    public void setTrabTelefono(String TrabTelefono) {
        this.TrabTelefono = TrabTelefono;
    }

    
}
